export function filterRestaurants(
  restaurants: Restaurant[],
  filters: {
    category?: string;
    priceRange?: string;
    search?: string;
  }
): Restaurant[] {
  return restaurants.filter((restaurant) => {
    // Category filter
    if (filters.category && filters.category !== 'all') {
      if (restaurant.category !== filters.category) return false;
    }

    // Price range filter
    if (filters.priceRange && filters.priceRange !== 'all') {
      const priceFilter = priceRanges.find((p) => p.id === filters.priceRange);
      if (priceFilter) {
        const avgPrice = (restaurant.priceValue.min + restaurant.priceValue.max) / 2;
        if (priceFilter.max && avgPrice > priceFilter.max) return false;
        if (priceFilter.min && avgPrice < priceFilter.min) return false;
      }
    }

    // Search filter
    if (filters.search) {
      const searchLower = filters.search.toLowerCase();
      const matchesName = restaurant.name.toLowerCase().includes(searchLower);
      const matchesNameKo = restaurant.nameKo.includes(filters.search);
      const matchesDescription = restaurant.description.toLowerCase().includes(searchLower);
      const matchesMenu = restaurant.recommendedMenu.some(
        (menu) => menu.toLowerCase().includes(searchLower) || menu.includes(filters.search!)
      );
      if (!matchesName && !matchesNameKo && !matchesDescription && !matchesMenu) {
        return false;
      }
    }

    return true;
  });
}
